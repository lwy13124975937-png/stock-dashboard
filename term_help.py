def get_term(term):
    return TERM_HELP.get(term, {
        "definition": "暂无详细定义。",
        "plain": "暂无大白话解释。",
        "usage": "暂无使用说明。",
        "warning": "请结合其他数据一起观察。",
    })


def render_term(st, term, label="ⓘ"):
    info = get_term(term)
    if hasattr(st, "popover"):
        with st.popover(label):
            render_term_body(st, term, info)
    else:
        with st.expander(f"{term} {label}"):
            render_term_body(st, term, info)


def render_term_body(st, term, info):
    st.markdown(f"**{term}**")
    st.markdown(f"**定义：** {info['definition']}")
    st.markdown(f"**大白话：** {info['plain']}")
    st.markdown(f"**怎么看：** {info['usage']}")
    st.markdown(f"**注意事项：** {info['warning']}")
    st.caption(RISK_TIP)


def render_glossary(st, terms, title="📘 本页术语速查表"):
    with st.expander(title, expanded=False):
        for term in terms:
            info = get_term(term)
            render_term_body(st, term, info)
            st.divider()


def label_with_info(st, text, term=None):
    cols = st.columns([1, 0.12])
    cols[0].markdown(text)
    with cols[1]:
        render_term(st, term or text)


